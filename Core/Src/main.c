void tick_count_handler()
{
	++tc.cnt;
	tc.m_1ms = 1;
	if(tc.cnt % 10 == 0)
	{
		tc.m_10ms = 1;
	}
	if(tc.cnt % 100 == 0)
	{
		tc.m_100ms = 1;
	}
	if(tc.cnt % 1000 == 0)
	{
		tc.m_1s = 1;
		tc.cnt = 0;
	}
}
